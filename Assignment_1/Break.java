package Assignment_1;

public class Break {
	    public static void main(String[] args) {
	        for (int i = 1; i <= 10; i++) {
	            if (i == 6) {
	                System.out.println("Breaking the loop at: " + i);
	                break;
	            }
	            System.out.println("Current number: " + i);
	        }
	    }
	}


